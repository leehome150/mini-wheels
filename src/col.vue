<template>
    <div class="col" :class="colClass"
         :style="colStyle">
        <slot></slot>
    </div>
</template>

<script>
    let validator = function (value) {
        let keys = Object.keys(value)
        let valid = true;
        keys.forEach(key => {
            if (!['span', 'offset'].includes(key)) {
                valid = false
            }
        })
        return valid
    }

    export default {
        data() {
            return {
                gutter: 0
            }

        },
        computed: {
            colClass() {
                let {span, offset, ipad, narrowPc, pc, widePc} = this
                return [`col-${span}`,
                    offset && `offset-${offset}`,
                    ...(ipad && [`col-ipad-${ipad.span}`]),
                    ...(narrowPc && [`col-narrowPc-${narrowPc.span}`]),
                    ...(pc && [`col-pc-${pc.span}`]),
                    ...(widePc && [`col-widePc-${widePc.span}`]),
                ]
            },
            colStyle() {
                return {
                    paddingLeft: this.gutter / 2 + 'px', paddingRight: this.gutter / 2 + 'px'
                }
            },

        },
        props: {
            span: {
                type: [Number, String]
            },
            offset: {
                type: [Number, String]
            },
            ipad: {
                type: Object,
                validator,
            },
            narrowPc: {
                type: Object,
                validator,
            },

            pc: {
                type: Object,
                validator,
            },
            widePc: {
                type: Object,
                validator,
            }
        },


    };
</script>

